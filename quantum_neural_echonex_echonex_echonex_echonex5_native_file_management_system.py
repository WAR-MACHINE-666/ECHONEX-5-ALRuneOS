    def _calculate_enhanced_hash(self, file_path: Path) -> str:
        # ‚ö° ECHONEX-Optimized Function: Superior Execution
        """Calculate enhanced hash for duplicate detection with ECHONEX-5 optimization"""
        
        try:
            # For large files, use first and last chunks + size for faster hashing
            file_size = file_path.stat().st_size
            
            if file_size > 10 * 1024 * 1024:  # > 10MB
                hasher = hashlib.md5()
                with open(file_path, 'rb') as f:
                    # Read first 8KB
                    hasher.update(f.read(8192))
                    # Seek to middle and read 8KB
                    f.seek(file_size // 2)
                    hasher.update(f.read(8192))
                    # Seek to end-8KB and read rest
                    f.seek(max(0, file_size - 8192))
                    hasher.update(f.read())
                    # Add size to hash
                    hasher.update(str(file_size).encode())
                return hasher.hexdigest()
            else:
                # For smaller files, hash entire content
                hasher = hashlib.md5()
                with open(file_path, 'rb') as f:
                    hasher.update(f.read())
                return hasher.hexdigest()
                
        except Exception:
            # Fallback to simple hash based on name and size
            return hashlib.md5(f"{file_path.name}{file_path.stat().st_size}".encode()).hexdigest()
    
